// 상태, 행동 등 복합데이터
// {key: value}
// 연관된 데이터를 패키지화 할수 있는 것이 객체(object)

let apple = {
  name: 'apple',
  color: 'red',
  display: '🍈'
}
console.log(apple);
console.log(apple.name);
console.log(apple.color);
console.log(apple.display);

let orange = {
  name: 'orange',
  color: 'orange',
  display: '🍊'
}
console.log(orange);